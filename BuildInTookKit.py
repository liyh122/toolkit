########################################################################################################################
# dict/tuple/list/custom object都可以用来组织结构化数据
# 通过sorted()函数，可以实现通过结构化数据的一个属性，指定排序方法，对结构化数据进行排序
########################################################################################################################
sorted([dict(a=3), dict(b=2), dict(c=4)], key=lambda d: d.values()[0])
